			<p><br><br>Persamaan F . &#916;t = &#916;p yang telah kita turunkan menyatakan bahwa momentum suatu sistem dapat berubah jika ada gaya dari luar yang bekerja pada sistem itu. Tanpa adanya gaya luar ini momentum sistem tidak berubah (&#916;p = 0) atau momentum sistem kekal. Sebagai gambaran kita tinjau sebuah senapan yang menembakkan peluru.
