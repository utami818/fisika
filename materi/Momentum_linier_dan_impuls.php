			<br>Percepatan roket diperoleh dari tolakan gas yang disemburkan roket itu. Tiap molekul gas dapat dianggap sebagai suatu peluru kecil yang ditembakkan roket. Dalam sistem ini momentum total roket dan momentum gas senantiasa sama selama tidak ada gaya luar (diabaikan). Jika gaya gravitasi yang bertindak sebagai gaya luar tidak diabaikan, ia akan mengurangi momentum roket. 
			<br>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Misalkan mula-mula kecepatan roket v dan massa roket m, anggap roket menyemburkan gas sejumlah &#916;m, sehingga kecepatan bertambah menjadi v+&#916;v. Kecepatan semburan gas anggap sebesar vg (Catatan: kecepatan roket dan kecepatan gas diukur relatif terhadap suatu acuan, misalnya bumi).
			<br> Momentum mula-mula roket: mv 
			<br>Momentum akhir roket: (m - &#916;m) (v + &#916;v) 
			<br>Momentum gas: - &#916;mvg 
			<br>Jika gravitasi diabaikan kita dapat menghitung besarnya pertambahan kecepatan Î”v dengan kekekalan momentum. 
			<br><br>
		\(P_awal=P_akhir\)
		<br>\(mv=(m-&#916;m)+(v+&#916;v)-&#916;mv_g\)
		<br>\(mv=mv-&#916;m.v+m.&#916;v-&#916;m.&#916;v-&#916;mv_g\)
		<br>\(0\ \ \ =-&#916;m.v+m.&#916;v-&#916;mv_g\)
		<br>\(&#916;v={&#916;m(v+v_g)\over m}\)
			
			<br><br>Catatan:
			<br> \(&#916;m . &#916;v\) diabaikan karena kecil dibandingkan dengan suku yang lain (&#916;m saja sudah kecil apalagi jika dikalikan dengan &#916;v tentu lebih kecil lagi bukan?)
			Percepatan roket dapat dihitung sebagai berikut:
			<br>
		\[a=\frac{&#916;v}{&#916;t}=\frac{v+v_g}{m}. \frac{&#916;m}{&#916;t}\]	
			<br>
			<br> &#916;m/&#916;t sering disebut laju semburan gas (banyaknya semburan gas tiap detik). v + vg adalah sama dengan kecepatan roket relatif terhadap gas dan sering ditulis vr sehingga persamaan percepatan rata-rata roket adalah:
			<br>
		\[a=\frac{v_r}{m}.\frac{&#916;m}{&#916;t}\]
			<br><br>Jika medan gravitasi tidak diabaikan, medan gravitasi akan memberikan percepatan ke bawah sehingga percepatan roket (atau sering disebut dengan percepatan lontar) menjadi:
			<br>
		\[a=\frac{v_r}{m}.\frac{&#916;m}{&#916;t}-g.................rumus(1)\]
