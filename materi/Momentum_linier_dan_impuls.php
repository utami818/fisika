			<br> Apakah benar momentum sistem tidak berubah? Bukankah momentum peluru mengalami perubahan setelah penembakan?
			<br> Memang benar momentum peluru mengalami perubahan yaitu dari nol (sebelum penembakan), menjadi tidak nol (sesudah penembakan)!
			<br>Akan tetapi kita harus ingat bahwa senapan juga mengalami perubahan momentum. Momentum senapan setelah penembakan ini sama dengan momentum peluru, tetapi arahnya berlawanan. Akibatnya momentum sistem (momentum senapan + momentum peluru) sama dengan nol, yaitu sama dengan momentum mula-mula. Dengan kata lain momentum kekal.
